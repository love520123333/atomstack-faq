def main():
    with open(INPUT, 'r', encoding='utf-8') as fh:
        content = fh.read()

    counts = count_faqs_by_machine(content)
    print("当前各机型 FAQ 数量:")
    targets = ['m-r8','m-r6','m-f80','m-d5','m-fb2','m-ae85','m-h1','m-f4n']
    for mid in targets:
        print(f"  {mid}: {counts.get(mid, 0)}")
    print(f"  总计: {sum(counts.values())}")

    max_id = get_max_faq_id(content)
    print(f"\n当前最大 FAQ ID: {max_id}")

    # 所有待写入的FAQ（按机型分组），只补够 20 条即可
    all_groups = [
        ('m-r8',   R8_FAQS,   4),
        ('m-r6',   R6_FAQS,   3),
        ('m-f80',  F80_FAQS,  3),
        ('m-d5',   D5_FAQS,   6),
        ('m-fb2',  FB2_FAQS,  7),
        ('m-ae85', AE85_FAQS, 8),
        ('m-h1',   H1_FAQS,   9),
        ('m-f4n',  F4N_FAQS,  12),
    ]

    entries = []
    faq_id = max_id + 1
    for mid, faq_list, need in all_groups:
        current = counts.get(mid, 0)
        to_add = max(0, 20 - current)
        if to_add == 0:
            print(f"  {mid} 已达到 20 条，跳过")
            continue
        added = 0
        for item in faq_list:
            if added >= to_add:
                break
            _mid, title, priority, tags, keywords, summary, solution, content_text = item
            entry = generate_faq_entry(
                faq_id, mid, title, priority, tags, keywords, summary, solution, content_text
            )
            entries.append(entry)
            faq_id += 1
            added += 1
        print(f"  {mid}: 将添加 {added} 条 FAQ（从 {current} → {current+added}）")

    if not entries:
        print("\n无需添加，所有机型已达到 20 条以上。")
        return

    # 找插入点
    insert_patterns = ['  ]\n}', '  ]\r\n}', '  ]\r}', ']\n}', ']\r\n}']
    insert_pos = -1
    for pat in insert_patterns:
        pos = content.rfind(pat)
        if pos != -1:
            insert_pos = pos
            insert_len = 3  # '  ]'
            break

    if insert_pos == -1:
        insert_pos = content.rfind(']')
        if insert_pos == -1:
            print("错误：找不到插入位置！")
            return
        insert_len = 1

    new_content = (
        content[:insert_pos]
        + '\n' + '\n'.join(entries) + '\n'
        + content[insert_pos:]
    )

    with open(INPUT, 'w', encoding='utf-8') as fh:
        fh.write(new_content)

    print(f"\n成功写入 {len(entries)} 条 FAQ，文件已更新。")

    # 验证写入
    with open(INPUT, 'r', encoding='utf-8') as fh:
        new_content_check = fh.read()
    new_counts = count_faqs_by_machine(new_content_check)
    print("\n写入后各机型 FAQ 数量:")
    for mid in targets:
        before = counts.get(mid, 0)
        after = new_counts.get(mid, 0)
        status = "OK" if after >= 20 else "NEED"
        print(f"  {mid}: {before} → {after} {status}")
    total = sum(new_counts.values())
    print(f"  总计: {sum(counts.values())} → {total}")

if __name__ == '__main__':
    main()
