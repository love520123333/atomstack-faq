def count_faqs_by_machine(content):
    counts = {}
    for m in re.finditer(r"f\('(m-[^']+)'", content):
        mid = m.group(1)
        counts[mid] = counts.get(mid, 0) + 1
    return counts

def get_max_faq_id(content):
    ids = [int(x) for x in re.findall(r"'faq-(\d+)'", content)]
    return max(ids) if ids else 0

def generate_faq_entry(faq_id, mid, title, priority, tags, keywords, summary, solution, content_text):
    tag_str = ', '.join(f"'{t}'" for t in tags)
    kw_str  = ', '.join(f"'{k}'" for k in keywords)
    lines = [
        f"    f('{mid}', 'faq-{faq_id}', '{title}', '{priority}',",
        f"      [{tag_str}],",
        f"      [{kw_str}],",
        f"      '{summary}',",
        f"      '{solution}',",
        f"      '{content_text}'),",
    ]
    return '\n'.join(lines)

