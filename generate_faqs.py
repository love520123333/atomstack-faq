# 为每个主题生成 FAQ 数据
def generate_faq(machine_id, machine_name, topic):
    if topic not in TEMPLATES:
        return None
    t = TEMPLATES[topic]
    title = t['title'].format(name=machine_name)
    
    # 为每个 FAQ 分配不同的优先级
    priorities = ['critical', 'high', 'medium', 'low']
    import random
    random.seed(hash(machine_id + topic))
    priority = random.choices(priorities, weights=[1, 3, 4, 2])[0]
    
    # 关键词
    keywords = f"{machine_name},{topic},排查,解决"
    
    return {
        'mid': machine_id,
        'title': title,
        'priority': priority,
        'tags': t['tags'],
        'keywords': keywords,
        'summary': t['summary'],
        'solution': t['solution'],
        'content': t['content'],
    }

# 生成所有 FAQ
all_faqs = []
for mid, name, desc, level, topics in MACHINES:
    for topic in topics:
        faq = generate_faq(mid, name, topic)
        if faq:
            all_faqs.append(faq)

