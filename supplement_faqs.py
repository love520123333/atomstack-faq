def generate_faq_entry(mid, name, title_suffix, priority, tags, kw, summary, sol_title, steps):
    """Generate a single FAQ f() call. mid should include 'm-' prefix."""
    title = f'{name} {title_suffix}'
    keywords = f'{name},{kw}'
    content = f'## {sol_title}\n\n{steps}'
    # Escape backticks and dollar signs in content for template literal
    content = content.replace('`', "'")
    content = content.replace('$', '')
    
    # Format as JS code - use 'm-' prefixed mid
    full_id = f'm-{mid}' if not mid.startswith('m-') else mid
    lines = [f"    f('{full_id}', '{title}', '{priority}', {tags},"]
    lines.append(f"      '{keywords}',")
    lines.append(f"      '{summary}',")
    lines.append(f"      '{sol_title}',")
    lines.append(f"      `{content}`)")
    return '\n'.join(lines)


