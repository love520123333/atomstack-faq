def main():
    with open(INPUT, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Count current FAQs per machine
    current = {}
    for m in re.finditer(r"f\('m-([^']+)'", content):
        mid = m.group(1)
        current[mid] = current.get(mid, 0) + 1
    
    print("Current FAQ counts:")
    for mid, count in sorted(current.items(), key=lambda x: -x[1]):
        needed = max(0, TARGET - count)
        print(f"  {mid}: {count} (need {needed} more)")
    
    # Generate supplementary FAQs
    new_faqs = {}
    for mid, count in current.items():
        if count >= TARGET:
            continue
        
        needed = TARGET - count
        templates = get_faqs_for_machine(mid)
        name = MACHINE_NAMES.get(mid, '')
        
        if not templates:
            print(f"  WARNING: No templates for {mid}")
            continue
        
        # Pick templates that don't already exist in the file
        existing_titles = set()
        for m in re.finditer(r"f\('m-[^']+',\s*'([^']+)'", content):
            existing_titles.add(m.group(1))
        
        added = 0
        for template in templates:
            if added >= needed:
                break
            title_suffix = template[0]
            full_title = f'{name} {title_suffix}'
            if full_title not in existing_titles:
                entry = generate_faq_entry(mid, name, *template)
                new_faqs.setdefault(mid, []).append(entry)
                existing_titles.add(full_title)
                added += 1
        
        print(f"  {mid}: adding {added} FAQs")
    
    if not new_faqs:
        print("\nAll machines already have enough FAQs!")
        return
    
    # Insert new FAQs before the closing `]` of the return array
    # Find the last `]` followed by `}` in the file
    insert_text = '\n'
    for mid, entries in new_faqs.items():
        insert_text += f'\n    // ================ {MACHINE_NAMES.get(mid, mid)} FAQ (supplement) ================\n'
        insert_text += '\n'.join(entries)
        insert_text += '\n'
    
    # Find the last closing bracket of the return array
    # Try multiple patterns for cross-platform compatibility
    patterns = ['  ]\n}', '  ]\r\n}', '  ]\r}', ']\n}', ']\r\n}']
    insert_pos = -1
    for p in patterns:
        idx = content.rfind(p)
        if idx != -1:
            insert_pos = idx
            break
    
    if insert_pos == -1:
        # Fallback: find last "]\n" before final "}"
        idx = content.rfind(']')
        if idx != -1:
            insert_pos = idx
        else:
            print("ERROR: Could not find insertion point")
            return
    
    new_content = content[:insert_pos] + insert_text + content[insert_pos:]
    
    with open(OUTPUT, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"\nDone! Added supplementary FAQs to {OUTPUT}")


if __name__ == '__main__':
    main()
